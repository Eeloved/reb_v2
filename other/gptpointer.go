package main

import (
	"fmt"
)

func main() {
	// Объявляем переменную A как указатель на int
	var A *int

	// Объявляем переменную B с произвольным целочисленным значением
	B := 42

	// Присваиваем в переменную A указатель на переменную B
	A = &B

	// Выводим значение B через разыменование указателя A
	fmt.Println("Значение через указатель A:", *A)

	// Меняем значение переменной B через указатель A
	*A = 100

	// Выводим новое значение переменной B
	fmt.Println("Новое значение переменной B:", B)
}
